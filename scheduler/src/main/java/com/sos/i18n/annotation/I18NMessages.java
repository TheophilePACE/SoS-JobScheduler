package com.sos.i18n.annotation;

import java.lang.annotation.Documented;
import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Simply an annotation that allows for multiple {@link I18NMessage} annotations to be defined.
 *
 * @author <a href="mailto:jmazzitelli@users.sourceforge.net">John Mazzitelli</a>
 */
@Documented
@Retention(RetentionPolicy.RUNTIME)
//@Target(ElementType.LOCAL_VARIABLE)
@Target({ElementType.FIELD, ElementType.LOCAL_VARIABLE})
public @interface I18NMessages {
	/**
	 * The individual messages being defined.
	 */
	I18NMessage[] value();

	/**
	 * \fn msgnum
	 * \brief msgnum
	 * 
	 * \details
	 *
	 * \return String
	 *
	 * @return the number of the message, if available
	 */
	String msgnum() default "";

	String msgurl() default "";

}
